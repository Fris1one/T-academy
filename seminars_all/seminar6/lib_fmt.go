package main

import "fmt"

func main() {
    // Общие форматирующие глаголы
    fmt.Println("=== Общие форматирующие глаголы ===")
    
    // %v - значение в стандартном формате
    fmt.Printf("%v - значение в стандартном формате\n", 42)
    fmt.Printf("%v - значение в стандартном формате\n", "Hello")
    fmt.Printf("%v - значение в стандартном формате\n", 3.14)
    
    // %#v - Go-синтаксическое представление значения
    fmt.Printf("%#v - Go-синтаксическое представление\n", "Go")
    fmt.Printf("%#v - Go-синтаксическое представление\n", []int{1, 2, 3})
    
    // %T - тип значения
    fmt.Printf("%T - тип значения\n", 100)
    fmt.Printf("%T - тип значения\n", "string")
    
    // %% - знак процента
    fmt.Printf("100%% - знак процента\n")
    
    fmt.Println("\n=== Логические значения ===")
    
    // %t - логическое значение (true/false)
    fmt.Printf("%t - логическое значение\n", true)
    fmt.Printf("%t - логическое значение\n", 10 > 5)
    
    fmt.Println("\n=== Целые числа ===")
    
    // %b - двоичное представление
    fmt.Printf("%b - двоичное представление\n", 42)
    
    // %c - символ, представленный соответствующим кодом Unicode
    fmt.Printf("%c - символ Unicode\n", 65) // A
    
    // %d - десятичное представление
    fmt.Printf("%d - десятичное представление\n", 42)
    
    // %o - восьмеричное представление
    fmt.Printf("%o - восьмеричное представление\n", 42)
    
    // %O - восьмеричное с префиксом 0o
    fmt.Printf("%O - восьмеричное с префиксом 0o\n", 42)
    
    // %q - одинарные кавычки, экранированный символ
    fmt.Printf("%q - в кавычках\n", "Go")
    fmt.Printf("%q - символ в кавычках\n", 'A')
    
    // %x - шестнадцатеричное, буквы в нижнем регистре
    fmt.Printf("%x - шестнадцатеричное (нижний регистр)\n", 255)
    
    // %X - шестнадцатеричное, буквы в верхнем регистре
    fmt.Printf("%X - шестнадцатеричное (верхний регистр)\n", 255)
    
    // %U - формат Unicode (U+1234)
    fmt.Printf("%U - формат Unicode\n", 'A')
    
    fmt.Println("\n=== Числа с плавающей точкой ===")
    
    // %b - экспоненциальный формат без степени
    fmt.Printf("%b - экспоненциальный без степени\n", 3.14159)
    
    // %e - научная нотация (нижний регистр e)
    fmt.Printf("%e - научная нотация (e нижний регистр)\n", 1234.5678)
    
    // %E - научная нотация (верхний регистр E)
    fmt.Printf("%E - научная нотация (E верхний регистр)\n", 1234.5678)
    
    // %f - десятичная запись без экспоненты
    fmt.Printf("%f - десятичная без экспоненты\n", 3.14159)
    
    // %F - синоним для %f
    fmt.Printf("%F - синоним для %%f\n", 3.14159)
    
    // %g - %e для больших экспонент, иначе %f
    fmt.Printf("%g - компактное представление\n", 3.14159)
    fmt.Printf("%g - компактное представление\n", 1000000.0)
    
    // %G - %E для больших экспонент, иначе %F
    fmt.Printf("%G - компактное представление (верхний регистр)\n", 3.14159)
    
    fmt.Println("\n=== Строки и срезы ===")
    
    // %s - строка или срез в виде строки
    fmt.Printf("%s - строка\n", "Hello, World!")
    fmt.Printf("%s - срез байт как строка\n", []byte{'G', 'o'})
    
    // %q - строка в двойных кавычках
    fmt.Printf("%q - строка в кавычках\n", "Hello")
    
    // %x - шестнадцатеричное представление строки (нижний регистр)
    fmt.Printf("%x - строка в hex\n", "Go")
    
    // %X - шестнадцатеричное представление строки (верхний регистр)
    fmt.Printf("%X - строка в HEX\n", "Go")
    
    fmt.Println("\n=== Указатели ===")
    
    x := 42
    ptr := &x
    
    // %p - указатель (шестнадцатеричный)
    fmt.Printf("%p - указатель\n", ptr)
    
    fmt.Println("\n=== Ширина и точность ===")
    
    // Ширина поля
    fmt.Printf("|%10s| - ширина 10 символов\n", "Go")
    fmt.Printf("|%-10s| - выравнивание по левому краю\n", "Go")
    
    // Для чисел - заполнение нулями
    fmt.Printf("%05d - заполнение нулями\n", 42)
    
    // Точность для чисел с плавающей точкой
    fmt.Printf("%.2f - точность 2 знака после запятой\n", 3.14159)
    fmt.Printf("%8.2f - ширина 8, точность 2\n", 3.14159)
    
    // Точность для строк
    fmt.Printf("%.5s - только первые 5 символов строки\n", "Hello, World!")
    
    fmt.Println("\n=== Составные примеры ===")
    
    // Несколько аргументов
    name := "Alice"
    age := 30
    height := 1.75
    fmt.Printf("Имя: %s, Возраст: %d, Рост: %.2f\n", name, age, height)
    
    // Индексы аргументов
    fmt.Printf("%[2]d %[1]s - изменение порядка аргументов\n", "World", 2024)
    
    // Форматирование структур
    type Person struct {
        Name string
        Age  int
    }
    p := Person{"Bob", 25}
    fmt.Printf("%v - структура\n", p)
    fmt.Printf("%+v - структура с именами полей\n", p)
    fmt.Printf("%#v - Go-синтаксис структуры\n", p)
}